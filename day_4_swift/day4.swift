func countOccurrences(of word: String, in grid: [[Character]]) -> Int {
    let directions = [
        (0, 1),    // Right
        (0, -1),   // Left
        (1, 0),    // Down
        (-1, 0),   // Up
        (1, 1),    // Down-Right
        (-1, -1),  // Up-Left
        (1, -1),   // Down-Left
        (-1, 1)    // Up-Right
    ]
    
    let wordArray = Array(word)
    let wordLength = wordArray.count
    let rows = grid.count
    let cols = grid[0].count
    var count = 0
    
    func isWordAt(_ x: Int, _ y: Int, direction: (Int, Int)) -> Bool {
        for i in 0..<wordLength {
            let nx = x + i * direction.0
            let ny = y + i * direction.1
            if nx < 0 || ny < 0 || nx >= rows || ny >= cols || grid[nx][ny] != wordArray[i] {
                return false
            }
        }
        return true
    }
    
    for x in 0..<rows {
        for y in 0..<cols {
            if grid[x][y] == wordArray[0] {
                for direction in directions {
                    if isWordAt(x, y, direction: direction) {
                        count += 1
                    }
                }
            }
        }
    }
    
    return count
}

func countXMASCrosses(in grid: [[Character]]) -> Int {
    let rows = grid.count
    let cols = grid[0].count
    let word = "MAS"
    let wordArray = Array(word)
    var count = 0
    
    func matchesMAS(_ diagonal: [(Int, Int)]) -> Bool {
        let letters = diagonal.compactMap { coord -> Character? in
            let (nx, ny) = coord
            guard nx >= 0, ny >= 0, nx < rows, ny < cols else { return nil }
            return grid[nx][ny]
        }
        return letters == wordArray || letters == wordArray.reversed()
    }
    
    for x in 1..<rows-1 {
        for y in 1..<cols-1 {
            // Define diagonals for a potential X-MAS cross
            let topLeftToBottomRight = [(x-1, y-1), (x, y), (x+1, y+1)]
            let topRightToBottomLeft = [(x+1, y-1), (x, y), (x-1, y+1)]
            
            // Check if both diagonals form "MAS"
            if matchesMAS(topLeftToBottomRight) && matchesMAS(topRightToBottomLeft) {
                count += 1
            }
        }
    }
    
    return count
}

